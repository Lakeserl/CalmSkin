-- Gift-with-purchase: products given free when a FREE_GIFT promotion applies.
-- Child table of promotions, like bundle_items.

CREATE TABLE promotion_gifts (
    id              BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id    BIGINT      NOT NULL REFERENCES promotions(id) ON DELETE CASCADE,
    product_id      BIGINT      NOT NULL,
    variant_id      BIGINT,
    quantity        INT         NOT NULL DEFAULT 1
);
CREATE INDEX idx_promotion_gifts_promotion ON promotion_gifts(promotion_id);
