-- CalmSKIN promotion-service schema.
-- userId / created_by are UUID (matches the running system; PROJECT_NOTES
-- BIGINT is a known doc error). Money is NUMERIC(15,2). Scope id lists are
-- stored as comma-separated TEXT.

CREATE TABLE promotions (
    id                      BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code                    VARCHAR(50)     UNIQUE,
    name                    VARCHAR(255)    NOT NULL,
    description             TEXT,
    type                    VARCHAR(30)     NOT NULL,
    discount_type           VARCHAR(20)     NOT NULL,
    discount_value          NUMERIC(15,2)   NOT NULL,
    max_discount_amount     NUMERIC(15,2),
    min_order_value         NUMERIC(15,2)   NOT NULL DEFAULT 0,
    min_item_quantity       INT             NOT NULL DEFAULT 0,
    applicable_product_ids  TEXT,
    applicable_category_ids TEXT,
    applicable_brand_ids    TEXT,
    excluded_product_ids    TEXT,
    total_usage_limit       INT,
    per_user_limit          INT             NOT NULL DEFAULT 1,
    starts_at               TIMESTAMP       NOT NULL,
    ends_at                 TIMESTAMP       NOT NULL,
    status                  VARCHAR(20)     NOT NULL DEFAULT 'DRAFT',
    is_stackable            BOOLEAN         NOT NULL DEFAULT FALSE,
    priority                INT             NOT NULL DEFAULT 0,
    created_by              UUID            NOT NULL,
    created_at              TIMESTAMP       NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMP       NOT NULL DEFAULT NOW(),
    CONSTRAINT chk_promotion_dates CHECK (ends_at > starts_at)
);
CREATE INDEX idx_promotions_status_time ON promotions(status, starts_at, ends_at);
CREATE INDEX idx_promotions_code        ON promotions(code) WHERE code IS NOT NULL;
CREATE INDEX idx_promotions_type_status ON promotions(type, status);

CREATE TABLE promotion_tiers (
    id              BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id    BIGINT          NOT NULL REFERENCES promotions(id) ON DELETE CASCADE,
    min_quantity    INT,
    min_value       NUMERIC(15,2),
    discount_type   VARCHAR(20)     NOT NULL,
    discount_value  NUMERIC(15,2)   NOT NULL,
    sort_order      SMALLINT        NOT NULL DEFAULT 0
);
CREATE INDEX idx_promotion_tiers_promotion ON promotion_tiers(promotion_id);

CREATE TABLE flash_sales (
    id                  BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id        BIGINT          NOT NULL REFERENCES promotions(id) ON DELETE CASCADE,
    product_id          BIGINT          NOT NULL,
    variant_id          BIGINT,
    original_price      NUMERIC(15,2)   NOT NULL,
    sale_price          NUMERIC(15,2)   NOT NULL,
    quantity_limit      INT             NOT NULL,
    quantity_sold       INT             NOT NULL DEFAULT 0,
    quantity_reserved   INT             NOT NULL DEFAULT 0,
    CONSTRAINT uq_flash_product UNIQUE (promotion_id, product_id, variant_id)
);
CREATE INDEX idx_flash_sales_promotion ON flash_sales(promotion_id);
CREATE INDEX idx_flash_sales_product   ON flash_sales(product_id);

CREATE TABLE bundle_items (
    id              BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id    BIGINT      NOT NULL REFERENCES promotions(id) ON DELETE CASCADE,
    product_id      BIGINT      NOT NULL,
    variant_id      BIGINT,
    quantity        INT         NOT NULL DEFAULT 1
);
CREATE INDEX idx_bundle_items_promotion ON bundle_items(promotion_id);

CREATE TABLE voucher_assignments (
    id              BIGINT      GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id    BIGINT      NOT NULL REFERENCES promotions(id) ON DELETE CASCADE,
    user_id         UUID        NOT NULL,
    assigned_at     TIMESTAMP   NOT NULL DEFAULT NOW(),
    expires_at      TIMESTAMP,
    source          VARCHAR(50),
    CONSTRAINT uq_voucher_user UNIQUE (promotion_id, user_id)
);
CREATE INDEX idx_voucher_assignments_user ON voucher_assignments(user_id);

-- Immutable audit trail of promotions applied to confirmed orders.
CREATE TABLE promotion_usages (
    id              BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id    BIGINT          NOT NULL REFERENCES promotions(id),
    user_id         UUID            NOT NULL,
    order_id        VARCHAR(100)    NOT NULL,
    discount_amount NUMERIC(15,2)   NOT NULL,
    applied_at      TIMESTAMP       NOT NULL DEFAULT NOW(),
    status          VARCHAR(20)     NOT NULL DEFAULT 'APPLIED',
    CONSTRAINT uq_usage_order_promo UNIQUE (order_id, promotion_id)
);
CREATE INDEX idx_usages_promotion_id ON promotion_usages(promotion_id);
CREATE INDEX idx_usages_user_id      ON promotion_usages(user_id);

-- Short-lived holds taken while an order is being created. order_id is NOT
-- unique here: one order may lock several promotions (one row each).
CREATE TABLE promotion_usage_locks (
    id              BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    promotion_id    BIGINT          NOT NULL REFERENCES promotions(id) ON DELETE CASCADE,
    user_id         UUID            NOT NULL,
    order_id        VARCHAR(100)    NOT NULL,
    discount_amount NUMERIC(15,2)   NOT NULL,
    locked_at       TIMESTAMP       NOT NULL DEFAULT NOW(),
    expires_at      TIMESTAMP       NOT NULL,
    CONSTRAINT uq_lock_promo_user UNIQUE (promotion_id, user_id)
);
CREATE INDEX idx_locks_order_id   ON promotion_usage_locks(order_id);
CREATE INDEX idx_locks_expires_at ON promotion_usage_locks(expires_at);
