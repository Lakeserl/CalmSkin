-- Per-order flash-sale slot reservations. A promotion usage lock may hold
-- slots across several flash-sale lines, so reservations are tracked per
-- (flash_sale, order) and reconciled on confirm / release / expiry.

CREATE TABLE flash_sale_reservations (
    id              BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    flash_sale_id   BIGINT          NOT NULL REFERENCES flash_sales(id) ON DELETE CASCADE,
    order_id        VARCHAR(100)    NOT NULL,
    quantity        INT             NOT NULL,
    created_at      TIMESTAMP       NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_flash_reservation UNIQUE (flash_sale_id, order_id)
);
CREATE INDEX idx_flash_reservations_order ON flash_sale_reservations(order_id);
